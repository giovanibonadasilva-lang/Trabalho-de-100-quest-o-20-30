n1=(float(input("screva o primeiro numero")))
n2=(float(input("escreva o segundo numero")))   

if n1 == n2:
    print("São iguais")
if n1 != n2:
    print("São diferentes")
if n1 > n2:
    print(("O maior numero é "),(f"{n1:.01f}"))
if n1 < n2:
    print(("O menor numero é "),(f"{n1:.01f}"))